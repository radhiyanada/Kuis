public class DaftarNilai {
    String nama;
    int nilaiPertama;
    int nilaiKedua;
    int nilaiKetiga;

    public DaftarNilai(String nama, int nilaiPertama, int nilaiKedua, int nilaiKetiga) {
        this.nama = nama;
        this.nilaiPertama = nilaiPertama;
        this.nilaiKedua = nilaiKedua;
        this.nilaiKetiga = nilaiKetiga;
    }
}
